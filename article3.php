                                <p>To be able to effectively mine cryptocurrency there are some supporting and infrastructural technologies that make it possible to mine cryptocurrency and they can be defined as the computational power and energy consumption needed to mine cryptocurrency.</p>
                                <p>The hardware that enables cryptocurrency mining is always equipped with ASICs which means an application-specific integrated circuit and it is an integrated circuit chip. An ASIC miner refers to a computerized device or hardware that uses ASICs for the sole purpose of "mining" digital currency.</p>
                                <p>Because of the computational power needed to mine cryptocurrency using special hardware like  ASICs, large amounts of energy is needed and consumed in these mining process. It is reported that as of 2020 the network consumes 120 gigawatts (GW) per second. This converts to about 63 terawatt-hours (TWh) per year and goes to show that a lot of energy is needed as part of the technological infrastructure of mining cryptocurrency.</p>
                                <p>While there are legal ways to mine cryptocurrency there are also illegal ways and one of such ways illegal uses of technologies of mining cryptocurrency is through crypto mining malware or cryptojacking which is malware that co-opts the target’s computing resources in order to mine cryptocurrency like bitcoin.</p>
                                <p>There are two main approaches cybercriminals use in the illegal use of technologies for mining cryptocurrency. First, by exploiting the ransomware malware family to encrypt a victim’s data and receive a payment using untraceable cryptocurrency in order to let the victim recover the encrypted information.</p>
                                <p>The second alternative consists of using botnets or executing illegal processes in browsers (cryptojacking) to add surreptitiously the computer resources of the victims, without their consent, to an ASIC-resistant mining pool and spend computational resources for the benefit of the criminal.</p>
                                <p>There have also been illegal approaches in how people consume and pay for the energy needed to mine cryptocurrency and it is reported that in China a Bitcoin manufacturer generated 150 MWh of illegal electricity in a production facility built using 200 computers and also in the Netherlands, electricity trafficking observed on farms has increased by 20% in the last five years and in another case that occurred in the Netherlands, a company manager was fired because he set up a secret mining system in the company’s server room.</p>
                                
