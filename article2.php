                                <p>As the digital art NFT industry continues to grow and gain popularity there also exists a lingering concern of NFTs being used to perpetrate several types of criminal activities.</p>
                                <p>One very evident danger of NFTs is how it is facilitating the theft of artwork because people can steal your artwork from online sources and mint as an NFT and sell it for their own gain without your knowledge and not even giving the original creator any type of benefit.</p>
                                <p>The art community has been so preoccupied with art theft and copyright NFTs, without the realization that someone can attach a nude to an NFT is truly horrifying. Someone can just sell a photo of your body without permission. In the wrong hands, NFTs can make revenge pornography to be a more worrying issue than it already is right now.</p>
                                <p>Another form of art theft through NFTs is a criminal scheme called ‘art heist’ which is when criminals exploit an NFT marketplace by hacking into the accounts of users registered on marketplaces and transferring digital art NFT to other accounts. The criminals can then sell digital art and then withdraw the proceeds.</p>
                                <p>The NFT boom of digital art has also given rise to ‘self-laundering’ which is a financial crime where criminals purchase NFTs using illegal funds and then go on to sell these NFTs to themselves, to create a record of sales on a blockchain.</p>
                                <p>The aim of creating this record through the ‘self-laundering’ scheme is for the criminals to sell the digital art to ignorant 3rd party buyers who would have purchased cryptocurrency with legitimate funds which then allows the criminals to exchange clean money with money gotten from illegal activities.</p>
                                <p>For a criminal organization using NFTs to clean money, they most likely will use a large network of cryptocurrency wallets and may even attempt to move the proceeds through a cryptocurrency exchange to add more steps between themselves and the final wallet where the crypto will be exchanged for fiat currency.</p>
                                <p>Digital art sellers have also been accused of tax evasion because many government agencies have not given any official instructions on the taxation of NFTs. Many sellers of NFTs have made profits from selling digital art and have not declared or filed any taxes on them.</p>
                                
